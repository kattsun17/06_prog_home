#include <stdio.h>     // 標準入出力のヘッダファイル
#include <string.h>    // 文字列処理のヘッダファイル

#define  LEN  8    // 文字列長

//====================================================================
//  大域宣言
//====================================================================


//====================================================================
//  本体関数
//====================================================================

int main(void) 
{
    //----  局所宣言
    char ope[LEN];    // 演算名
    int n1, n2;       // 演算引数

    //----  入力処理
    // 演算名と2つの引数の入力
    scanf("%s", ope);
    scanf("%d", &n1);
    scanf("%d", &n2);

    //printf("%d", strcmp(ope,"plus\0"));
    //puts(ope);

	//----  計算と出力 
    // 加算
    if ( strcmp(ope,"plus") == 0 ) {
        // 和の出力
        printf("%d", n1 + n2);;
    }
    // 減算
    else if ( strcmp(ope,"minus") == 0 ) {
        // 差の出力
        printf("%d", n1 - n2);
    }
    // 乗算
    else if ( strcmp(ope,"multi") == 0 ) {
        // 積の出力
        printf("%d", n1 * n2);
    }
    // 除算
    else if ( strcmp(ope,"div") == 0 ) {
        // 商の出力
        printf("%d", n1 / n2);
    }
    // エラーメッセージの出力
    else { printf("ERR"); }	
    //----  終了処理
    return 0;
}


